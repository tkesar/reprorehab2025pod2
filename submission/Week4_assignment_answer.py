# We will be using RR interval data.
rr_sub1 = pd.read_csv('https://physionet.org/files/rr-interval-healthy-subjects/1.0.0/007.txt?download',
                      header=None, names=['sub7'])
rr_sub2 = pd.read_csv('https://physionet.org/files/rr-interval-healthy-subjects/1.0.0/009.txt?download',
                      header=None, names=['sub9'])
rr = rr_sub1.merge(rr_sub2, left_index=True, right_index=True)

fig3 = plt.figure(layout='constrained', figsize=(10, 6))
# 3a. Set the grids
# 3a.1. Provide nrow and ncol to `GridSpec`. Please note that the width ratio of
#       the three axes at the bottom row are 1:1:2.
gs = GridSpec(2, 4, figure=fig3)
# 3a.2. Use correct slices. Each input to the following four rows
#       needs to be `gs[row_number, column_range]
ax3a = fig3.add_subplot(gs[0, :])       # Top row, covering all colums
ax3b = fig3.add_subplot(gs[1, :-3])       # Bottom row, covering the first column
ax3c = fig3.add_subplot(gs[1, 1:-2])       # Bottom row, covering the second column
ax3d = fig3.add_subplot(gs[1, 2:])       # Bottom row, covering the remaining columns

# 3b. Secondary y axis on ax3b
ts1 = rr['sub7'].iloc[0:200].to_numpy()
ts2 = rr['sub7'].iloc[10000:10200].to_numpy()
ts_diff = ts2 - ts1
ax3a2 = ax3a.twinx()
# 3b.1. Plot the raw data: rr['sub7'] & rr['sub9']
#       Label each as 'First 200 samples' and '50th 200 samples'
#       Set color as 'C1' and 'C2', respectively.
#       Set alpha to 0.5
ax3a.plot(ts1, label='First 200 samples', color='C1', alpha=0.5)
ax3a.plot(ts2, label='50th 200 samples', color='C2', alpha=0.5)
# 3b.2. Set the title: 'sub 7: Window difference'
ax3a.set_title('sub 7: Window difference')
# 3b.3. Set X axis label: 'Sample number' and Y axis label: 'RR interval (ms)'
ax3a.set_xlabel('Sample number')
ax3a.set_ylabel('RR interval (ms)')
# Plot the difference of the two timeseries along the secondary axis: ax3b2
# Use additional arguments: `color='C0', label='Difference'`
ax3a2.plot(ts_diff, color='C0', label='Difference')
ax3a2.set_ylabel("Interval difference (ms)")
# legends
lns = ax3a.get_lines() + ax3a2.get_lines()
labels = [l.get_label() for l in lns]
# Display legend on the upper right
ax3a.legend(lns, labels, loc='upper right')

# 3c. Histogram with custom bin labels
# 3c.1. Make bins to range from 500 to 1300, with dx=50 (Hint: use `np.arange`)
bins = range(500, 1300, 50)
# 3c.2. Draw a histogram of rr['sub7']. Provide `bins` and set alpha to 0.7 +
#       edgecolor to 'k' (black).
ax3b.hist(rr['sub7'], bins=bins, alpha=0.7, edgecolor='k')
# m: mean of the data; s: standard deviation of the data
m, s = rr['sub7'].mean(), rr['sub7'].std()
# 3c.3. Draw a vertical line showing the mean of the data.
#       Use `.axvline`. Set color to 'C1', linewidth (lw) to 2,
#       and label to f'mean={m:.1f}'
ax3b.axvline(x=m, color='C1', lw=2, label=f'mean={m:.1f}')
ax3b.axvspan(m-s, m+s, color='C1', alpha=0.15, label=f'+/- 1SD={s:.1f}')
# Add:
# - a title('sub 7')
# - X/Y axis labels('RR interval(ms)' & 'Count')
# - legend
ax3b.set_title('sub 7')
ax3b.set_xlabel('RR interval (ms)')
ax3b.set_ylabel('Count')
ax3b.legend()

# 3d. Histograms, overlaid
# 3d.1. Draw histograms, similar to 'Side-by-side' example of 'Plotting_in_Python.ipynb'
#       additional parameters: edgecolor='k', alpha=0.5, bins=25, label=??
ax3c.hist(rr, edgecolor='k', alpha=0.5, bins=25, label=rr.columns)
# 3d.2. Add X/Y axis labels and legend, same as 3c.
ax3c.set_xlabel('RR interval (ms)')
ax3c.set_ylabel('Count')
ax3c.legend()

# 3e. 2D histogram
# 3e.1. Use `.hist2d`. You should plot rr['sub7'] and rr['sub9'].
#       Use cmap='magma' and bins=35.
ax3d.hist2d(rr['sub7'], rr['sub9'], cmap='magma', bins=35)
# 3e.2. Set X/Y axis labels:
#       - 'sub 7, RR interval (ms)'
#       - 'sub 9, RR interval (ms)'
ax3d.set_xlabel('sub 7, RR interval (ms)')
ax3d.set_ylabel('sub 9, RR interval (ms)')
