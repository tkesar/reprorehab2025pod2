# %% import packages
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from matplotlib.gridspec import GridSpec

# %% loading data
data = pd.read_csv('https://physionet.org/files/gaitpdb/1.0.0/GaCo01_01.txt?download',
                   sep='\t', header=None,
                   names=['Time',
                          *[''.join(('LS', str(p))) for p in range(8)],
                          *[''.join(('RS', str(q))) for q in range(8)],
                          'Total_LS', 'Total_RS'])

# %% Preparing the dataset
ts = data['LS0'].to_numpy()
t = data['Time'].to_numpy()

# %% Moving average
k = 101
ma = np.convolve(ts, np.ones(k)/k, mode='same')

# %% Normalization (min-max)
ts_min, ts_max = ts.min(), ts.max()
ts_norm = (ts - ts_min) / (ts_max - ts_min)

# %% Task 1. Please draw a line plot
# 1a. Create a figure and an axis using `plt.subplots()`. Make the figure size 12 x 4 inches.
fig, ax = plt.subplots(1, 1, figsize=(12, 4))
# 1b. Plot 'ts' against 't'. Set the 'alpha' value to 0.3
ax.plot(t, ts, alpha=0.3)
# 1c. Plot 'ma' against 't'. Set the linewidth to 2 by inserting 'lw=2'.
#     Also add a label: 'Window size: k' where k is the variable defined above.
#     (Hint. Check how f-string was used under 'Modifying axis properties' in Plotting_in_Python.ipynb)
ax.plot(t, ma, lw=2, label=f'Window size: {k}')
ax.plot(t, ts_norm, ls='--', label='normalized (rescaled)')
# 1d. Set X-axis label: 'Time (s)'
ax.set_xlabel('Time (s)')
# 1e. Set Y-axis label: 'Force (N)'
ax.set_ylabel('Force (N)')
# 1f. Set the title: 'Time series with moving average & normaliztion'
ax.set_title('Time series with moving average & normalization')
# 1g. Display labels by running the line: `ax.legend()`
ax.legend()

# We will emphasize certain window (30-40s)
ax.axvspan(30, 40, color='C1', alpha=0.2)
# 1h. Add text: 'highlight'. Left position is 30.5, bottom position is `ts.max()*0.95`.
#     Set the fontsize to 10
ax.text(30.5, ts.max()*0.95, 'highlight', size=10)

# This is how you save a figure.
fig.savefig('Time_series.png', dpi=300)

# %% Task 2. Please draw a scatterplot
# 2a. Make a 12 x 6 inches figure. Prepare two axes, each occupying one column.
fig2, ax2 = plt.subplots(1, 2, figsize=(12, 6))
#####################
# Left: scatterplot #
#####################
# 2b. We will only use the first 100 points from each timeseries.
#     The first timeseries is data['LS0']. The second one is data['LS1']
#     Use `.iloc[0:100]` to trim the data you will visualize
#     Set the marker to '+' and 'alpha' to 0.6. Set 'color' to 'g'(green).
sub = data.iloc[0:100, :]
ax2[0].scatter(sub['LS0'], sub['LS1'], marker='+', alpha=0.6, color='g')
ax2[0].axis('equal')
# We will add lines indicating medians of the two timeseries
# 2c. Please get the median of the first 100 samples of data['LS0'] and data['LS1']
#     Save them to variables 'ls0_median' and 'ls1_median', respectively.
#     You can use `.median()`
ls0_median = sub['LS0'].median()
ls1_median = sub['LS1'].median()
# 2d. Use `.axhline()` and `.axvline()` to mark medians.
#     Do make use of the two variables you created above.
#     (Hint. Check how `.axhline()` is used under 'Modifying axis properties' in Plotting_in_Python.ipynb)
ax2[0].axhline(y=ls1_median, xmin=0, xmax=1, color='r', ls='--')
ax2[0].axvline(x=ls0_median, ymin=0, ymax=1, color='r', ls=':')
# Add texts: medians
ax2[0].text(30, ls1_median+10, f"Position 2 median:\n{ls1_median:.2f}N",
            color='C1')
ax2[0].text(ls0_median+10, 200, f"Position 1 median:\n{ls0_median:.2f}N",
            color='C0')
# 2e. Set X-axis label: 'Left foot position 1 (N)'
ax2[0].set_xlabel('Left foot position 1 (N)', color='C0', weight='bold')
# 2f. Set Y-axis label: 'Left foot position 2 (N)'
ax2[0].set_ylabel('Left foot position 2 (N)', color='C1', weight='bold')
#####################
# Right: Histogram  #
#####################
# 2g. Draw a stacked bar histogram on the right pane.
#     You need to provide the first 100 samples of 'LS0' and 'LS1' of 'data'.
ax2[1].hist(sub.loc[:, ['LS0', 'LS1']], histtype='barstacked',
            label=['Position 1', 'Position 2'],
            edgecolor='k')
ax2[1].set_xlabel("Force (N)")
ax2[1].set_ylabel("Count")
# 2h. Display legend
ax2[1].legend()

# 2i. Save the figure as 'Two_plots.png'. Set dpi=300.
fig2.savefig('Two_plots.png', dpi=300)

