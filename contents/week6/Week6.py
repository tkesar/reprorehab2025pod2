# Function parameters can have default values:
# Here, default values of the specified by:
#   `(name of a parameter)=(default value)`
# So here, the default of `num1` is `None`. The same is true for `num2`.
def add_nums(num1=None, num2=None):
  # `all(iterable)` returns True if everything in iterable is True.
  # So here, if `num1` is None and `num2` is None, it will be True
  if all((num1 is None, num2 is None)):
    # If conditional is True, return `None`.
    # A function 'ends' with a `return` command.
    return None
  # This is why you don't need explicit `else` statement here.
  # If `if` statement above is true, there's already an associated `return` statement,
  # so the function will no longer proceed. If `if` statement is not true,
  # following `return` statement will run.
  return num1 + num2

# This returns None, because no `num1` or `num2` value is provided.
# Both parameters will be `None`, and the function will return `None`
#   -> so you don't see anything returned
add_nums()

# If `num1` is not None or `num2` is not None, `num1 + num2` is returned.
add_nums(3, 7)

