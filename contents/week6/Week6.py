"""### Nested function / inner function

You can define a function inside a function. Of course, it will be local to the *outermost* function.
"""

def nested_function(val:list|None=None):
  """
  nested_function will show how you can nest a function
  inside another function.

  If `val` is not a list, return None.
  Otherwise, return a new list with each element doubled.

  Parameters
  ----------
  val: list|None (this means the expected input for `val` is either a list or None)
    a list to process; default to None

  Returns
  -------
  None if val is None, double_it(val) otherwise.
  """
  if not isinstance(val, list):
    return None

  def double_it(to_double:list):
    """Double the elemenfs of a provided list"""
    return [x * 2 for x in to_double]

  return double_it(val)

nested_function([1,2,3,4,5])

